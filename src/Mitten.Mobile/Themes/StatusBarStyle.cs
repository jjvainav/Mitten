namespace Mitten.Mobile.Themes
{
    /// <summary>
    /// Defines the style for the status bar on the device.
    /// </summary>
    public enum StatusBarStyle
    {
        /// <summary>
        /// Invalid.
        /// </summary>
        Invalid = 0,

        /// <summary>
        /// Indicates that the status bar should use a light style.
        /// </summary>
        Light,

        /// <summary>
        /// Indicates that the status bar should use a dark style.
        /// </summary>
        Dark
    }
}